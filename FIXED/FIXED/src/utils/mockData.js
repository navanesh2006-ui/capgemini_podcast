const generateEmotionTimeline = (ep) => {
  const steps = 10;
  const list = [];
  
  // Find podcast category
  const podcast = mockPodcasts.find(p => p.id === ep.podcastId) || {};
  const category = podcast.category || "General";

  // Create a deterministic hash from the episode ID and title to seed curves
  const seedString = ep.id + ep.title;
  let hash = 0;
  for (let i = 0; i < seedString.length; i++) {
    hash = seedString.charCodeAt(i) + ((hash << 5) - hash);
  }
  const seed = Math.abs(hash);

  // Determine emotional biases based on the podcast's category
  let excitementBias = 40;
  let curiosityBias = 40;
  let surpriseBias = 30;
  let deepThinkingBias = 40;
  let joyBias = 40;

  if (category === "Business") {
    excitementBias = 55;
    curiosityBias = 60;
    deepThinkingBias = 45;
    surpriseBias = 35;
    joyBias = 40;
  } else if (category === "Diet" || category === "Health") {
    curiosityBias = 65;
    deepThinkingBias = 55;
    joyBias = 50;
    excitementBias = 30;
    surpriseBias = 25;
  } else if (category === "Motivation") {
    excitementBias = 70;
    joyBias = 60;
    curiosityBias = 50;
    surpriseBias = 40;
    deepThinkingBias = 30;
  } else if (category === "Story") {
    surpriseBias = 60;
    curiosityBias = 60;
    joyBias = 45;
    excitementBias = 40;
    deepThinkingBias = 40;
  } else if (category === "Yoga" || category === "Mindfulness") {
    deepThinkingBias = 75;
    joyBias = 70;
    curiosityBias = 35;
    excitementBias = 15;
    surpriseBias = 15;
  }

  // Pre-generate dynamic quotes templates based on topics, hosts, and titles
  const quotesTemplates = [
    `Let's explore the core concepts of ${ep.title} led by ${ep.host}.`,
    `Discussing the key challenges and background of this session.`,
    `Analyzing the deep implications of ${category === "Yoga" ? "breathing flow" : "these structural dynamics"}.`,
    `Evaluating the strategic ideas that ${ep.host} highlighted.`,
    `A surprising breakthrough that changes how we think about this.`,
    `Sharing practical advice and next steps for the listeners.`,
    `Concluding thoughts and final wisdom from ${ep.host}.`
  ];

  for (let i = 0; i < steps; i++) {
    const timeSec = Math.floor((ep.durationSeconds / (steps - 1)) * i);
    const min = Math.floor(timeSec / 60);
    const sec = timeSec % 60;
    const timeStr = `${min.toString().padStart(2, "0")}:${sec.toString().padStart(2, "0")}`;

    // Use sine waves with unique seeds (frequencies and phases) for each emotion
    // so they form beautiful, smooth, intersecting curves
    const exc = Math.min(100, Math.max(10, Math.round(excitementBias + Math.sin(i * 1.2 + (seed % 10)) * 20 + Math.cos(i * 0.5) * 5)));
    const cur = Math.min(100, Math.max(10, Math.round(curiosityBias + Math.sin(i * 0.9 + (seed % 7)) * 18 + Math.sin(i * 0.3) * 6)));
    const sur = Math.min(100, Math.max(10, Math.round(surpriseBias + Math.cos(i * 1.5 + (seed % 13)) * 22 + Math.sin(i * 0.6) * 4)));
    const dep = Math.min(100, Math.max(10, Math.round(deepThinkingBias + Math.sin(i * 0.7 + (seed % 5)) * 16 + Math.cos(i * 0.4) * 8)));
    const joy = Math.min(100, Math.max(10, Math.round(joyBias + Math.cos(i * 1.1 + (seed % 11)) * 17 + Math.sin(i * 0.5) * 5)));

    // Find the dominant emotion at this point
    const emotionsList = [
      { name: "Excitement", val: exc },
      { name: "Curiosity", val: cur },
      { name: "Surprise", val: sur },
      { name: "Deep Thinking", val: dep },
      { name: "Joy", val: joy }
    ];
    emotionsList.sort((a, b) => b.val - a.val);
    const dominant = emotionsList[0].name;

    // Build unique quotes combining templates and category-specific terms
    let quote = quotesTemplates[(i + (seed % 3)) % quotesTemplates.length];
    if (i === 0) {
      quote = `Welcome to "${ep.title}". We are starting our analysis.`;
    } else if (i === steps - 1) {
      quote = `Thanks for tuning in. That wraps up this segment.`;
    }

    list.push({
      timestamp: timeStr,
      timestampSeconds: timeSec,
      excitement: exc,
      curiosity: cur,
      surprise: sur,
      deepThinking: dep,
      joy: joy,
      dominant: dominant,
      quote: quote
    });
  }
  return list;
};

