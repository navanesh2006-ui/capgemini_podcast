  clean = clean.replace(/^(hello,\s+welcome\s+to\s+the\s+big\s+boss\s+interview\.\s+i'm\s+[a-z\s]+,\s+)/i, "");
  clean = clean.replace(/^(hello,\s+welcome\s+to\s+)/i, "");
  clean = clean.replace(/^(hello,\s+and\s+welcome\s+to\s+)/i, "");
  clean = clean.replace(/^(so\s+here\s+is\s+my\s+conversation\s+with\s+)/i, "");
  clean = clean.replace(/^(welcome\s+to\s+the\s+big\s+boss\s+interview)/i, "");
  
  clean = clean.replace(/\b(you know|like|sort of|um|uh|well)\b,?\s*/gi, "");
  clean = clean.replace(/\s+/g, " ").trim();
  
