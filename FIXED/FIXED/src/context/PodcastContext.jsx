export const getEpisodeTopics = (ep, knowledge) => {
  if (!ep) return [];
  if (EPISODE_TOPICS[ep.id]) {
    return EPISODE_TOPICS[ep.id];
  }

  const topics = [];

  // Extract from summary paragraphs
  if (knowledge?.summary?.paragraphs) {
    knowledge.summary.paragraphs.forEach((p, index) => {
      const words = p.toLowerCase()
        .replace(/[^\w\s]/g, "")
        .split(/\s+/)
        .filter(w => w.length > 4);
      
      topics.push({
        keywords: Array.from(new Set(words)).slice(0, 6),
        concept: `Summary Insight ${index + 1}`,
        explanation: p,
        notes: `The episode summary notes: "${p.substring(0, 100)}..."`,
        implication: "This shows how user-uploaded content is dynamically structured into semantic modules."
      });
    });
  }

  // Extract from notes
  if (knowledge?.notes) {
    knowledge.notes.forEach((note, index) => {
      const words = note.content.toLowerCase()
        .replace(/[^\w\s]/g, "")
        .split(/\s+/)
        .filter(w => w.length > 4);

      topics.push({
        keywords: Array.from(new Set(words)).slice(0, 6),
        concept: `Key Takeaway ${index + 1}`,
        explanation: note.content,
        notes: `The host notes that "${note.content}"`,
        implication: "This contextual point helps clarify the primary thesis of the upload."
      });
    });
  }

  if (topics.length === 0) {
    const desc = ep.description || "custom upload details";
    const words = desc.toLowerCase()
      .replace(/[^\w\s]/g, "")
      .split(/\s+/)
      .filter(w => w.length > 4);

    topics.push({
      keywords: Array.from(new Set(words)).slice(0, 6),
      concept: "Overview",
      explanation: desc,
      notes: `Presented by ${ep.host || "the host"}.`,
      implication: "This represents a newly uploaded podcast ready for interactive analysis."
    });
  }

  return topics;
};

export const getTimestampExplanation = (currentTime, activeEpisode, timeline, epTopics) => {
  if (!activeEpisode) return "";

  const segments = [
    { name: "Introduction", description: "opening remarks, setting the stage, and introducing the host and the main themes" },
    { name: "Core Thesis", description: "the foundational argument and core concept that forms the basis of the discussion" },
    { name: "Contextual Background", description: "historical context, scientific background, or industry trends relevant to the topic" },
    { name: "Deep Analysis", description: "a detailed breakdown of the primary mechanisms and data points involved" },
    { name: "Key Challenges", description: "the main hurdles, societal concerns, or technical bottlenecks faced in this domain" },
    { name: "Alternative Viewpoints", description: "diverse perspectives, debate points, or alternative interpretations of the facts" },
    { name: "Practical Applications", description: "real-world implications, actionable advice, and how these concepts are applied in practice" },
    { name: "Future Outlook", description: "predictions, upcoming breakthroughs, and long-term implications of these developments" },
    { name: "Key Takeaways", description: "a summary of the host's primary conclusions and the most important things to remember" },
    { name: "Closing Remarks", description: "final thoughts, contact info, and wrap-up of the episode" }
  ];

  const duration = activeEpisode.durationSeconds || 1800;
  const segmentIndex = Math.min(9, Math.floor((currentTime / duration) * 10));
  const activeSegment = segments[segmentIndex];

  let closestPoint = null;
  if (timeline && timeline.length > 0) {
    let minDiff = Infinity;
    for (const point of timeline) {
      const diff = Math.abs(point.timestampSeconds - currentTime);
      if (diff < minDiff) {
        minDiff = diff;
        closestPoint = point;
      }
    }
  }

  const quoteStr = closestPoint ? closestPoint.quote : "exploring key concepts";
  const dominantEmotion = closestPoint ? closestPoint.dominant : "Curiosity";

  const openers = [
    `At the playhead position of **${formatTimeHelper(currentTime)}** (approx. ${Math.round((currentTime / duration) * 100)}% of the timeline), the discussion is in the **${activeSegment.name}** phase, which covers ${activeSegment.description}.`,
    `Currently, at **${formatTimeHelper(currentTime)}** into the playback, the host is addressing the **${activeSegment.name}** of the episode, focusing on ${activeSegment.description}.`,
    `We are in the **${activeSegment.name}** phase of the episode (at **${formatTimeHelper(currentTime)}**). This section highlights ${activeSegment.description}.`
  ];

  const hashVal = currentTime;
  const opener = openers[hashVal % openers.length];

  let conceptualExplanation = "";
  if (epTopics && epTopics.length > 0) {
    let bestTopic = null;
    let maxMatch = 0;
    const cleanQuote = quoteStr.toLowerCase();
    
    for (const topic of epTopics) {
      let matchCount = 0;
      for (const kw of topic.keywords) {
        if (cleanQuote.includes(kw.toLowerCase())) {
          matchCount++;
        }
      }
      if (matchCount > maxMatch) {
        maxMatch = matchCount;
        bestTopic = topic;
      }
    }

    if (bestTopic) {
      conceptualExplanation = ` This relates directly to **${bestTopic.concept}**: the host details how ${bestTopic.explanation}`;
    }
  }

  const toneExplanation = ` The emotional tone here registers high **${dominantEmotion}** as the host asserts: *"${quoteStr}"*.`;
  
  return `${opener}${toneExplanation}${conceptualExplanation}`;
};

